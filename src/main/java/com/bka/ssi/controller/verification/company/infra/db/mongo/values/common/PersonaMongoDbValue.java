package com.bka.ssi.controller.verification.company.infra.db.mongo.values.common;

import org.springframework.data.mongodb.core.mapping.Field;

/**
 * The type Persona mongo db value.
 */
public class PersonaMongoDbValue {
    @Field("title")
    private String title;

    @Field("firstName")
    private String firstName;

    @Field("lastName")
    private String lastName;

    /**
     * Instantiates a new Persona mongo db value.
     */
    public PersonaMongoDbValue() {
    }

    /**
     * Gets title.
     *
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * Sets title.
     *
     * @param title the title
     */
    public void setTitle(String title) {
        this.title = title;
    }

    /**
     * Gets first name.
     *
     * @return the first name
     */
    public String getFirstName() {
        return firstName;
    }

    /**
     * Sets first name.
     *
     * @param firstName the first name
     */
    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    /**
     * Gets last name.
     *
     * @return the last name
     */
    public String getLastName() {
        return lastName;
    }

    /**
     * Sets last name.
     *
     * @param lastName the last name
     */
    public void setLastName(String lastName) {
        this.lastName = lastName;
    }
}
